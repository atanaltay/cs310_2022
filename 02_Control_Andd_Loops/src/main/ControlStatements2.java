package main;

public class ControlStatements2 {

	public static void main(String[] args) {
		
		//Logical Operators
		//&& -> and
		// || -> or
		
		System.out.println(true && false);
		System.out.println(true || false);
		
		
		int x=50;
		
		if(x>40 && x<60) {
			System.out.println("X is between 40 and 60");
		}
		
		
		
	}
	
	
}
